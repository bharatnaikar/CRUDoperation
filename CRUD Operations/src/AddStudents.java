private boolean validateInput() {
    String sid = jTextField1.getText();
    String name = jTextField2.getText();
    String email = jTextField3.getText();
    String age = jTextField4.getText();
    String sclass = jTextField5.getText();
    String dob = jTextField6.getText();
    String gender = choice1.getSelectedItem();
    String address = jTextField8.getText();

    // Validate Student ID (should be an integer)
    if (sid.isEmpty() || name.isEmpty() || email.isEmpty() || age.isEmpty() || sclass.isEmpty() || dob.isEmpty() || address.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all the fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    try {
        Integer.parseInt(sid); // Try to parse the Student ID to an integer
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Student ID must be a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate Name (should only contain alphabetic characters)
    if (!name.matches("[a-zA-Z]+")) {
        JOptionPane.showMessageDialog(this, "Name must contain only alphabetic characters.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate Email (should be in valid email format)
    if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid email address.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate Age (should be a number)
    try {
        Integer.parseInt(age);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Age must be a valid number.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate Class (should be an integer)
    try {
        Integer.parseInt(sclass);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Class must be a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate Address (should contain only alphabetic characters and spaces)
    if (!address.matches("[a-zA-Z ]+")) {
        JOptionPane.showMessageDialog(this, "Address must contain only alphabetic characters and spaces.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
    dateFormat.setLenient(false);  // Prevents invalid date entries like 30/02/2024
    try {
        dateFormat.parse(dob); // Try to parse the DOB
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "DOB must be in the format dd/MM/yyyy.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // If all validations pass, return true
    return true;
}

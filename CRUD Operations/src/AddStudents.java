        if (validateInput()) {
        // Proceed with database insertion if validation passes
        String sid = jTextField1.getText();
        String name = jTextField2.getText();
        String email = jTextField3.getText();
        String age = jTextField4.getText();
        String sclass = jTextField5.getText();
        String dob = jTextField6.getText();
        String gender = choice1.getSelectedItem();
        String address = jTextField8.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentlist", "root", "");
            Statement smt = con.createStatement();
            smt.executeUpdate("INSERT INTO studentdata VALUES (" + sid + ",'" + name + "','" + email + "'," + age + "," + sclass + ",'" + dob + "','" + gender + "','" + address + "')");
            JOptionPane.showMessageDialog(null, "Student data added successfully", "Alert", JOptionPane.INFORMATION_MESSAGE);
            con.close();
        } catch (ClassNotFoundException | SQLException e) {
            System.out.println(e);
        }
    }
        
